CREATE TABLE mlb_game_data(
	id INT AUTO_INCREMENT PRIMARY KEY,
    game_date DATE,
    home_team VARCHAR(50),
    away_team VARCHAR(50),
    home_starter_era FLOAT,
    away_starter_era FLOAT,
    home_starter_whip FLOAT,
    away_starter_whip FLOAT,
    home_starter_hr FLOAT,
    away_starter_hr FLOAT,
    home_starter_k FLOAT,
    away_starter_k FLOAT,
    home_starter_r FLOAT,
    away_starter_r FLOAT,
    home_team_era FLOAT,
    away_team_era FLOAT,
    home_pitcher_obp FLOAT,
    away_pitcher_obp FLOAT,
    home_ba FLOAT,
    away_ba FLOAT,
    home_slg FLOAT,
    away_slg FLOAT,
    home_obp FLOAT,
    away_obp FLOAT,
    home_ops FLOAT,
    away_ops FLOAT,
    line FLOAT,
    result VARCHAR(10)
);